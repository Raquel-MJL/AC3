CREATE DATABASE IF NOT EXISTS CochesDB;
USE CochesDB;
CREATE TABLE IF NOT EXISTS Coches(
Matricula CHAR(10) NOT NULL PRIMARY KEY,
Marca VARCHAR(20)NOT NULL COMMENT "Campo para la marca" ,
Modelo VARCHAR(25)NOT NULL COMMENT "Campo para el modelo",
Color VARCHAR(24)NOT NULL COMMENT "Campo para el color",
Precio DECIMAL(10,2)NOT NULL COMMENT "Campo para el precio"
);

